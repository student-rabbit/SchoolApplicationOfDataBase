-- 예시. 반바지의 종류는 의류, 판매수량은 5로 수정을 한다. ------------------------------------------------
/*
 UPDATE 제품판매현황
 SET 종류 = '의류', 판매수량 = 5
 WHERE 제품명 = '반바지'

 오류 발생: 뷰 또는 함수 '제품판매현황'은(는) 수정 시 여러 기본 테이블에 영향을 주므로 업데이트할 수 없습니다.
 뷰가 근거로 하고 있는 기본 태이블들(2개)에 영향을 준다.
 UPDATE라는 하나의 구문을 사용하지만 결과는 여러 개 테이블을 업데이트 한다. 
 한 번의 구문으로 두 개 이상의 테이블을 수정하는 것은 안 된다.
 하나씩 수정해야 한다.
*/

SELECT *
FROM 제품

SELECT *
FROM 판매

BEGIN TRAN

UPDATE 제품판매현황
SET 종류 = '의류'
WHERE 제품명 = '반바지'


UPDATE 제품판매현황
SET 판매수량 = 5
WHERE 제품명 = '반바지'
-- UPDATE 구문이 각각 실행되었다.

SELECT * 
FROM 제품판매현황

ROLLBACK

